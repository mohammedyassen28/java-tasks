


var radius = parseFloat(prompt("Enter the radius of the circle : "));
var circleArea = Math.pow(radius,2) * Math.PI ;
alert("The total area of the circle is : " + circleArea.toFixed(2));


var userValue = parseFloat(prompt("Enter the value that you want to calculate its square root : "));
var valueSquareRoot = Math.sqrt(userValue);
alert("Square root of " + userValue + " is : " + valueSquareRoot.toFixed(2));